import { QueryClient } from "@tanstack/react-query";

// QueryClient управляет серверными данными на frontend:
// кэширует ответы API, обновляет данные после мутаций и хранит состояния loading/error.
export const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      // Данные считаются свежими 30 секунд.
      // Это уменьшает количество лишних запросов при переходах между страницами.
      staleTime: 30_000,

      // Для дипломного проекта отключаем агрессивные повторы запросов,
      // чтобы ошибки API было проще видеть и объяснять.
      retry: 1,

      // При возвращении на вкладку браузера данные не будут автоматически перезапрашиваться.
      // Позже можно включить точечно для уведомлений или корзины.
      refetchOnWindowFocus: false,
    },
  },
});
